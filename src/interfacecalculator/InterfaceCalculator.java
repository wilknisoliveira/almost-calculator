    private void btnEqualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEqualActionPerformed
        String check="";
        
        //Prevent a error in Engine libray
        if(fullDisTxt!=""){
            //Verify is the last digit is a number (prevent error)
            check = Character.toString(fullDisTxt.charAt(fullDisTxt.length()-1));
            if(check.chars().allMatch(Character::isDigit)){
                calculus();
                lblDisplay.setText("");
                lastDisTxt = "";
            }
        }
    }//GEN-LAST:event_btnEqualActionPerformed
    
