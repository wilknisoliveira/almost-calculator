    //This function add the new entry to the expression
    private void keyButton(String key){
        
        /**
         * Verify if the last character of the string is a bracket and the new 
         * entry is a number or the opposite. If true, apply the times operator. 
         * This prevents an error in Engine library.
         * The error: 2(2 --> The Engine can't recognize as 2*(2.
         */
        String check="";
        if(fullDisTxt != ""){
            check = Character.toString(fullDisTxt.charAt(fullDisTxt.length()-1));
        }
        else
            check = "a";

        if(check.equals(")") && key.chars().allMatch(Character::isDigit)){
            fullDisTxt = fullDisTxt + "*";        
        }
        
        else if(key.equals("(") && check.chars().allMatch(Character::isDigit)){
            fullDisTxt = fullDisTxt + "*"; 
        }
        
        //Add the new Entry(key) in the expression. Also, prevent put a 0 in the beginning. 
        String disTxt = fullDisTxt;
        if(disTxt.equals("0")){
            fullDisTxt = key;
            lastDisTxt = key;
        }
        else{
            fullDisTxt = disTxt + key;
            lastDisTxt = lastDisTxt + key;
        }
        lblDisplay.setText(fullDisTxt);
    }
    
    //This function calculate the math expression with Engine library.
    private void calculus(){
        //To prevent a error, apply abrackets if the last brackets don't closed.
        if(bracket>0){
            
            for(int i=0; i<bracket; i++){
                fullDisTxt = fullDisTxt + ")";
            }
            
            try{
                Object localResult = engine.eval(fullDisTxt);
                lblResult.setText(localResult.toString());
            }catch (ScriptException e){
                //Show a error mensage.
                lblError.setText("Syntax Error");
                lblResult.setText("");
                e.printStackTrace();
            }
            
            //Erase the brackets after the calculation
            for(int i=0; i<bracket; i++){
                fullDisTxt = fullDisTxt.substring(0, fullDisTxt.length()-1);
            }
            
        }
        
        else{
            try{
                Object localResult = engine.eval(fullDisTxt);
                lblResult.setText(localResult.toString());
            }catch (ScriptException e){
                lblError.setText("Syntax Error");
                lblResult.setText("");
                e.printStackTrace();
            }
        }
    }
    
