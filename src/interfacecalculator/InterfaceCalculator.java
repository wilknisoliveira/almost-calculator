    private void btnDotActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDotActionPerformed
        float number = 0;
        
        //Prevent a error in parseFloat function
        if(lastDisTxt==""){}
        else
            number =  Float.parseFloat(lastDisTxt);
        
        //Improve the visual expression
        if(number==0){
            keyButton("0.");
        }
        
        //If the number is real (already have a dot), don't do nothing
        else if((int) number != number){}
        
        else{
            keyButton(".");
        }
        calculus();
    }//GEN-LAST:event_btnDotActionPerformed

